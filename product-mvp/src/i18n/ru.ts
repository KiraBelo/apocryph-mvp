// Widen literal string types to string so en.ts can use different values
type Widen<T> = T extends readonly string[]
  ? readonly string[]
  : T extends Record<string, string>
    ? Record<string, string>
    : T extends object
      ? { [K in keyof T]: Widen<T[K]> }
      : T extends string
        ? string
        : T

export type Translations = Widen<typeof ru>
